// Dose Calculator Helper: Calculates Paracetamol and Ibuprofen doses
export interface DoseRecommendation {
  paracetamol: {
    mg: number;
    syrup120mgMl: number; // 120mg / 5ml
    syrup250mgMl: number; // 250mg / 5ml
    frequency: string;
    warnings: string;
  };
  ibuprofen: {
    mg: number;
    syrup100mgMl: number; // 100mg / 5ml
    frequency: string;
    warnings: string;
    notRecommended: boolean;
  };
}

export function calculateDoses(weightKg: number, ageGroup: string): DoseRecommendation {
  // Paracetamol dose: 15 mg per kg
  const paracetamolMg = Math.round(weightKg * 15);
  // 120mg / 5ml => 1 ml contains 24 mg. So ml = mg / 24
  const paracetamol120Ml = parseFloat((paracetamolMg / 24).toFixed(1));
  // 250mg / 5ml => 1 ml contains 50 mg. So ml = mg / 50
  const paracetamol250Ml = parseFloat((paracetamolMg / 50).toFixed(1));

  // Ibuprofen dose: 10 mg per kg
  const ibuprofenMg = Math.round(weightKg * 10);
  // 100mg / 5ml => 1 ml contains 20 mg. So ml = mg / 20
  const ibuprofenMl = parseFloat((ibuprofenMg / 20).toFixed(1));

  const isInfantUnder6m = ageGroup === 'less-than-6m' || weightKg < 5.0;

  return {
    paracetamol: {
      mg: paracetamolMg,
      syrup120mgMl: paracetamol120Ml,
      syrup250mgMl: paracetamol250Ml,
      frequency: 'كل 4 إلى 6 ساعات عند الحاجة (بحد أقصى 4 مرات خلال 24 ساعة).',
      warnings: 'تنبيه: يجب عدم تجاوز الجرعة اليومية المسموح بها لتجنب الضغط على الكبد. لا تعطه مع أدوية أخرى تحتوي على الباراسيتامول.'
    },
    ibuprofen: {
      mg: ibuprofenMg,
      syrup100mgMl: ibuprofenMl,
      frequency: 'كل 6 إلى 8 ساعات عند الحاجة (بحد أقصى 3 مرات خلال 24 ساعة).',
      warnings: isInfantUnder6m 
        ? 'تحذير شديد: لا يُعطى الإيبوبروفين للأطفال الرضع دون سن 6 أشهر أو الذين يزنون أقل من 5 كجم إلا بتوجيه مباشر من طبيب الأطفال المختص.'
        : 'يفضل إعطاؤه بعد الرضاعة أو تناول الطعام لحماية جدار المعدة.',
      notRecommended: isInfantUnder6m
    }
  };
}

// Risk Evaluator: Evaluates risk based on temperature, symptoms and weight
export function evaluateRisk(temp: number, selectedSymptomIds: string[], childAge: string): RiskAssessmentResult {
  const hasCriticalSymptom = selectedSymptomIds.some(id => {
    const s = SYMPTOMS_LIST.find(item => item.id === id);
    return s ? s.isCritical : false;
  });

  const hasHighSymptom = selectedSymptomIds.some(id => {
    const s = SYMPTOMS_LIST.find(item => item.id === id);
    return s ? !s.isCritical : false;
  });

  const symptomCount = selectedSymptomIds.length;

  let level: RiskLevel = 'normal';
  let label = 'طبيعي';
  let colorClass = 'text-emerald-600 dark:text-emerald-400';
  let bgClass = 'bg-emerald-50 dark:bg-emerald-950/30';
  let borderClass = 'border-emerald-200 dark:border-emerald-900/50';
  let icon = 'ShieldCheck';
  let summary = 'درجة الحرارة في الحدود الطبيعية ولا توجد أعراض خطورة مقلقة حالياً.';
  let instructions = [
    'حافظ على تهوية جيدة لغرفة الطفل.',
    'قدم السوائل والرضاعة الطبيعية بانتظام لترطيب الجسم.',
    'راقب نشاط الطفل العام وتغذيته بشكل مستمر.'
  ];

  // Logic for neonates / young infants (under 6 months)
  const isVeryYoung = childAge === 'less-than-6m';

  if (temp >= 40.0 || (hasCriticalSymptom && temp >= 38.5) || (isVeryYoung && temp >= 39.0)) {
    level = 'critical';
    label = 'حرجة جداً - طوارئ فورية!';
    colorClass = 'text-red-700 dark:text-red-400 font-extrabold';
    bgClass = 'bg-red-50 dark:bg-red-950/40 animate-pulse';
    borderClass = 'border-red-400 dark:border-red-900';
    icon = 'Activity';
    summary = 'تنبيه طوارئ: هذه الحالة تعتبر حرجة جداً وتستدعي التوجه الفوري لأقرب مستشفى أو الاتصال بالدفاع المدني/الإسعاف. الحمى المرتفعة مع الأعراض العصبية تشكل خطراً مباشراً على دماغ طفل نقص الأكسجين.';
    instructions = [
      'اتصل بالإسعاف فوراً أو توجه لأقرب قسم طوارئ دون أي تأخير.',
      'أثناء انتظار الإسعاف أو في الطريق، انزع ملابس الطفل الزائدة.',
      'ضع كمادات ماء فاتر (وليس بارداً) على الرقبة وتحت الإبطين وأعلى الفخذين.',
      'إذا حدث تشنج: ضع الطفل على جانبه الأيمن، أبعد أي أجسام حادة، لا تضع أي شيء في فمه، واحسب مدة التشنج.'
    ];
  } else if (temp >= 39.0 || hasCriticalSymptom || (isVeryYoung && temp >= 38.0)) {
    level = 'high';
    label = 'مرتفعة الخطورة - استشارة عاجلة';
    colorClass = 'text-rose-600 dark:text-rose-400 font-bold';
    bgClass = 'bg-rose-50 dark:bg-rose-950/30';
    borderClass = 'border-rose-300 dark:border-rose-900/50';
    icon = 'AlertTriangle';
    summary = 'الحرارة مرتفعة بشكل مقلق أو توجد أعراض خطرة مصاحبة. لدى أطفال نقص الأكسجين، يتطلب هذا الوضع تدخلاً علاجياً سريعاً من فريق طبي لمنع حدوث تشنجات أو تلف للخلايا العصبية.';
    instructions = [
      'استشر طبيباً أو توجه لأقرب مركز صحي على الفور لفحص الطفل.',
      'أعطِ خافض حرارة مناسب لوزنه (باراسيتامول أو إيبوبروفين بالجرعة الموصى بها).',
      'ابدأ فوراً بالكمادات بالماء الفاتر لتبريد الأوعية الدموية الكبرى.',
      'احرص على تقديم جرعات صغيرة متكررة من السوائل لمنع الجفاف.'
    ];
  } else if (temp >= 38.1 || hasHighSymptom) {
    level = 'moderate';
    label = 'متوسطة الخطورة - تتطلب إدارة نشطة';
    colorClass = 'text-amber-600 dark:text-amber-400 font-semibold';
    bgClass = 'bg-amber-50 dark:bg-amber-950/20';
    borderClass = 'border-amber-200 dark:border-amber-900/40';
    icon = 'AlertCircle';
    summary = 'الطفل يعاني من حمى خفيفة إلى متوسطة. نظراً للتاريخ المرضي بنقص الأكسجين عند الولادة، يجب عدم إهمال هذه الحرارة والبدء فوراً ببروتوكول خفض الحرارة المنزلي والمراقبة اللصيقة.';
    instructions = [
      'أعطِ الطفل خافض الحرارة (باراسيتامول) بالجرعة المحسوبة بدقة بناءً على الوزن.',
      'طبق كمادات ماء فاتر على جبهة وجسم الطفل.',
      'راقب درجة الحرارة كل 30 دقيقة بدقة للتأكد من عدم استمرار صعودها.',
      'احرص على إبقاء ملابس الطفل خفيفة ومريحة وسقيه سوائل كافية.'
    ];
  } else if (temp >= 37.6) {
    level = 'mild';
    label = 'خطورة خفيفة - مراقبة مستمرة';
    colorClass = 'text-yellow-600 dark:text-yellow-400';
    bgClass = 'bg-yellow-50 dark:bg-yellow-950/10';
    borderClass = 'border-yellow-200 dark:border-yellow-900/30';
    icon = 'Info';
    summary = 'ارتفاع طفيف جداً في درجة الحرارة فوق الطبيعي (بداية حمى). يجب البدء بمراقبة مستمرة للتأكد من عدم تصاعدها بشكل مفاجئ.';
    instructions = [
      'خفف ملابس الطفل وتأكد من تهوية الغرفة بشكل جيد.',
      'أكثر من السوائل أو الرضاعة لمساعدة الجسم على تبريد نفسه تلقائياً.',
      'قم بقياس الحرارة مجدداً بعد ساعة أو استخدم جهاز مراقبة مستمرة لتفادي المفاجآت الليلية.'
    ];
  }

  return {
    level,
    label,
    colorClass,
    bgClass,
    borderClass,
    icon,
    summary,
    instructions
  };
}

